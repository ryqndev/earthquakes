export function viridis(t: number) {
    const a = [
        [0.267, 0.004, 0.329],
        [0.105, 1.404, -1.235],
        [-0.072, 0.065, 1.601],
        [0.233, -0.247, -1.006],
        [0.246, -0.615, -0.107],
        [0.067, -0.130, 0.649],
    ];

    t = Math.min(Math.max(t, 0), 1); // Ensure t is between 0 and 1

    const r = a[0][0] + t * (a[1][0] + t * (a[2][0] + t * (a[3][0] + t * (a[4][0] + t * a[5][0]))));
    const g = a[0][1] + t * (a[1][1] + t * (a[2][1] + t * (a[3][1] + t * (a[4][1] + t * a[5][1]))));
    const b = a[0][2] + t * (a[1][2] + t * (a[2][2] + t * (a[3][2] + t * (a[4][2] + t * a[5][2]))));


    return {
        r: (r),
        g: (g),
        b: (b)
    };
}
